// DetermineProvider provides unified provider detection with clear precedence:
// 1. Command-line flag (if provided)
// 2. Environment variable (SPROUT_PROVIDER, with LEDIT_PROVIDER backward-compat)
// 3. Config file (last_used_provider)
// 4. First available provider based on API keys (uses priority order + dynamic discovery)
// 5. Fallback to Ollama
func DetermineProvider(explicitProvider string, lastUsedProvider ClientType) (ClientType, error) {
	// 1. Command-line flag has highest priority
	if explicitProvider != "" {
		provider, err := ParseProviderName(explicitProvider)
		if err != nil {
			return "", fmt.Errorf("invalid provider '%s': %w", explicitProvider, err)
		}
		if !IsProviderAvailable(provider) {
			return "", fmt.Errorf("provider '%s' is not available (check API key)", explicitProvider)
		}
		return provider, nil
	}

	// 2. Environment variable
	if providerEnv := envutil.GetEnvSimple("PROVIDER"); providerEnv != "" {
		provider, err := ParseProviderName(providerEnv)
		if err == nil && IsProviderAvailable(provider) {
			return provider, nil
		}
	}

	// 3. Last used provider from config
	if lastUsedProvider != "" && IsProviderAvailable(lastUsedProvider) {
		return lastUsedProvider, nil
	}

	// 4. First available provider based on API keys
	// Priority order for recommended providers (new providers can be added to the end)
	priorityOrder := []ClientType{
		ClientType("openrouter"),
		ClientType("zai"),
		ClientType("deepinfra"),
		ClientType("deepseek"),
		ClientType("minimax"),
		ClientType("cerebras"),
		ClientType("chutes"),
		ClientType("openai"),
		ClientType("mistral"),
		ClientType("lmstudio"),
		ClientType("ollama-cloud"),
		OllamaLocalClientType,
	}

	for _, provider := range priorityOrder {
		if IsProviderAvailable(provider) {
			return provider, nil
		}
	}

	// 5. Final fallback
	return OllamaLocalClientType, nil
}

// ParseProviderName converts a string provider name to ClientType
// Handles special aliases and validates provider names.
func ParseProviderName(name string) (ClientType, error) {
	normalized := strings.ToLower(strings.TrimSpace(name))

	// Handle special aliases first (these are not in provider configs)
	switch normalized {
	case "ollama":
		// "ollama" maps to ollama-local
		return OllamaLocalClientType, nil
	case "test":
		return TestClientType, nil
	case "editor":
		return EditorClientType, nil
	case "":
		return "", fmt.Errorf("provider name cannot be empty")
	}

	// For other providers, return as ClientType - validation happens later
	// This allows for custom providers and new providers to be discovered
	return ClientType(normalized), nil
}

// IsProviderAvailable checks if a provider can be used.
// Uses credentials.HasProviderCredential to avoid hardcoding env var strings.
func IsProviderAvailable(provider ClientType) bool {
	return credentials.HasProviderCredential(string(provider))
}

// GetProviderName returns the human-readable name for a provider
func GetProviderName(clientType ClientType) string {
	// Mapping of provider client types to display names
	// This is kept in sync with providers.ProviderDisplayNames() to avoid circular dependencies
	providerDisplayNames := map[ClientType]string{
		OpenAIClientType:      "OpenAI",
		OpenRouterClientType:  "OpenRouter (Recommended)",
		ZAIClientType:         "Z.AI",
		ZAICodingClientType:   "GLM Coding Plan",
		DeepInfraClientType:   "DeepInfra",
		DeepSeekClientType:    "DeepSeek",
		LMStudioClientType:    "LM Studio",
		MistralClientType:     "Mistral",
		MinimaxClientType:     "MiniMax",
		ChutesClientType:      "Chutes",
		CerebrasClientType:    "Cerebras",
		OllamaCloudClientType: "Ollama (Cloud)",
		OllamaLocalClientType: "Ollama (Local)",
		TestClientType:        "Test Provider",
		EditorClientType:      "Editor Mode",
	}

	// Check if we have a display name for this provider
	if displayName, ok := providerDisplayNames[clientType]; ok {
		return displayName
	}

	// Handle the "ollama" alias that maps to ollama-local
	if string(clientType) == "ollama" {
		return "Ollama (Local)"
	}

	// Fallback to string representation for unknown/custom providers
	return string(clientType)
}

